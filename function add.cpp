#include<iostream>
using namespace std;
int add(int a,int b)
 {
 	 return a+b;
 	 return a-b;
 	 return a*b;
 	 return a/b;
 	 return a%b;
 }
   int main()
   {
   	 int a,b;
   	 cin>>a>>b;
   	 add(a,b);
   	 cout<<"a and b:"<<a+b<<endl;
   	 cout<<"a and b:"<<a-b<<endl;
   	 cout<<"a and b:"<<a*b<<endl;
   	 cout<<"a and b:"<<a/b<<endl;
   	 cout<<"a and b:"<<a%b;
   	 return 0;
   }
